package model;

public class EnterAndFinish {

	public int enterCount;
	public int finishCount;
	public int enterCountDistinct;
	public int finishCountDistinct;

	public int getEnterCount() {
		return enterCount;
	}

	public void setEnterCount(int enterCount) {
		this.enterCount = enterCount;
	}

	public int getFinishCount() {
		return finishCount;
	}

	public void setFinishCount(int finishCount) {
		this.finishCount = finishCount;
	}

	public int getEnterCountDistinct() {
		return enterCountDistinct;
	}

	public void setEnterCountDistinct(int enterCountDistinct) {
		this.enterCountDistinct = enterCountDistinct;
	}

	public int getFinishCountDistinct() {
		return finishCountDistinct;
	}

	public void setFinishCountDistinct(int finishCountDistinct) {
		this.finishCountDistinct = finishCountDistinct;
	}

}
