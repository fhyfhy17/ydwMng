package model;

public class ScoreBuySum {

	public int scoreitemindex;
	public String scoreitemname;
	public int scoreprice;
	public int buysum;
	public int num;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getScoreitemindex() {
		return scoreitemindex;
	}

	public void setScoreitemindex(int scoreitemindex) {
		this.scoreitemindex = scoreitemindex;
	}

	public String getScoreitemname() {
		return scoreitemname;
	}

	public void setScoreitemname(String scoreitemname) {
		this.scoreitemname = scoreitemname;
	}

	public int getScoreprice() {
		return scoreprice;
	}

	public void setScoreprice(int scoreprice) {
		this.scoreprice = scoreprice;
	}

	public int getBuysum() {
		return buysum;
	}

	public void setBuysum(int buysum) {
		this.buysum = buysum;
	}

}
